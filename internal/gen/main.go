// Command gen produces the repetitive higher-arity Record and Select code for
// the jooq package. It is invoked by the //go:generate directive in doc.go and
// writes record_gen.go and select_gen.go into the parent jooq package directory.
//
// Arities 1 through 5 are written by hand; this program emits 6 through 22.
package main

import (
	"fmt"
	"go/format"
	"log"
	"os"
	"strings"
)

const (
	minArity = 6
	maxArity = 22
)

func main() {
	if err := write("record_gen.go", generateRecords()); err != nil {
		log.Fatal(err)
	}
	if err := write("select_gen.go", generateSelects()); err != nil {
		log.Fatal(err)
	}
}

// write formats source with gofmt and writes it to path.
func write(path, source string) error {
	formatted, err := format.Source([]byte(source))
	if err != nil {
		return fmt.Errorf("formatting %s: %w\n--- source ---\n%s", path, err, source)
	}
	return os.WriteFile(path, formatted, 0o644)
}

// typeParams returns "T1, T2, ..., TN".
func typeParams(n int) string {
	parts := make([]string, n)
	for i := range parts {
		parts[i] = fmt.Sprintf("T%d", i+1)
	}
	return strings.Join(parts, ", ")
}

const header = "// Code generated by go run ./internal/gen; DO NOT EDIT.\n\npackage gooq\n\n"

func generateRecords() string {
	var b strings.Builder
	b.WriteString(header)
	for n := minArity; n <= maxArity; n++ {
		tp := typeParams(n)
		fmt.Fprintf(&b, "// Record%d holds %d typed column values.\n", n, n)
		fmt.Fprintf(&b, "type Record%d[%s any] struct {\n", n, tp)
		for i := 1; i <= n; i++ {
			fmt.Fprintf(&b, "\tV%d T%d\n", i, i)
		}
		b.WriteString("}\n\n")

		// Values()
		fmt.Fprintf(&b, "func (r Record%d[%s]) Values() []any {\n\treturn []any{", n, tp)
		for i := 1; i <= n; i++ {
			if i > 1 {
				b.WriteString(", ")
			}
			fmt.Fprintf(&b, "r.V%d", i)
		}
		b.WriteString("}\n}\n\n")

		// Get(i)
		fmt.Fprintf(&b, "func (r Record%d[%s]) Get(i int) any {\n\tswitch i {\n", n, tp)
		for i := 1; i <= n; i++ {
			fmt.Fprintf(&b, "\tcase %d:\n\t\treturn r.V%d\n", i-1, i)
		}
		b.WriteString("\t}\n\treturn nil\n}\n\n")
	}
	return b.String()
}

func generateSelects() string {
	var b strings.Builder
	b.WriteString(header)
	b.WriteString("import \"database/sql\"\n\n")
	for n := minArity; n <= maxArity; n++ {
		tp := typeParams(n)
		record := fmt.Sprintf("Record%d[%s]", n, tp)

		// signature parameters: f1 Field[T1], ..., fN Field[TN]
		params := make([]string, n)
		fields := make([]string, n)
		scans := make([]string, n)
		for i := 1; i <= n; i++ {
			params[i-1] = fmt.Sprintf("f%d Field[T%d]", i, i)
			fields[i-1] = fmt.Sprintf("f%d", i)
			scans[i-1] = fmt.Sprintf("&r.V%d", i)
		}

		fmt.Fprintf(&b, "// Select%d begins a SELECT projecting %d typed columns.\n", n, n)
		fmt.Fprintf(&b, "func Select%d[%s any](%s) SelectFromStep[%s] {\n",
			n, tp, strings.Join(params, ", "), record)
		fmt.Fprintf(&b, "\treturn newSelect([]node{%s}, func(rows *sql.Rows) (%s, error) {\n",
			strings.Join(fields, ", "), record)
		fmt.Fprintf(&b, "\t\tvar r %s\n", record)
		fmt.Fprintf(&b, "\t\terr := rows.Scan(%s)\n", strings.Join(scans, ", "))
		b.WriteString("\t\treturn r, err\n\t})\n}\n\n")
	}
	return b.String()
}
